// Writing a java code to calculate the Harry Age 
// Create AgeOfHarry class to compute the age of Harry in 2024
public class AgeOfHarry
{
  public static void main(String [] args)
  {
    // Create a variable birthYear to store the harry's birth year
    int birthYear = 2000;

    // Create a variable currentYear to store the current year
    int currentYear = 2024;

    // Display the Harry's present age 
    System.out.println("Harry's age in 2024 is: " + (currentYear-birthYear));
  }
}